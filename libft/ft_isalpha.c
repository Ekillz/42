#include "libft.h"

int ft_isalpha(int c)
{
	if ((t_ui)c >= 'A' && (t_ui)c <= 'Z')
		return (1);
	else if ((t_ui)c >= 'a' && (t_ui)c <= 'z')
		return (1);
	else
		return (0);
	return (0);
}
