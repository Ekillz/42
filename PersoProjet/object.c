#include "header.h"

void object(t_env *e)
{
	e->x1 = 400;
	e->y1 = 100;
	e->max1 = 500;
	e->may1 = 800;
	while (e->y1++ < e->may1)
	{
		e->x1 = 400;
		while (e->x1++ < e->max1)
			mlx_pixel_put(e->mlx, e->win, e->x1, e->y1, 0xFFFFFF);
		usleep(5000);
	}
}
